package practice_problem;

public class Question_07 {

	public static void main(String[] args) {
		System.out.println("Before exchange");
		int x=5;
		int y=2;
		int z;
		System.out.println("x="+x+""+"y="+y);
		System.out.println("After Exchange");
		z=x;
		x=y;
		y=z;
		System.out.println("x="+x+""+"y="+y);
		
	}

}
